let nomesCompetidores = ["Daniel", "Rafael", "Manoel"]
let colocacao = [1, 2, 3]
function primeiroLugar(nomesCompetidores, colocacao) {
    if (nomesCompetidores, colocacao) {
        nomesCompetidores[0] == "Daniel" && colocacao[0] == 1
        return colocacao[0] + " " + nomesCompetidores[0]
    }
}
console.log(primeiroLugar(nomesCompetidores, colocacao))
